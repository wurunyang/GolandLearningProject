package main

import (
	"fmt"
	"sync"
)

type Fetcher interface {
	// Fetch 返回 URL 的 body 内容，并且将在这个页面上找到的 URL 放到一个 slice 中。
	Fetch(url string) (body string, urls []string, err error)
}

// Crawl 使用 fetcher 从某个 URL 开始递归的爬取页面，直到达到最大深度。
func Crawl(url string, depth int, fetcher Fetcher, known *Safemap) {
	// TODO: 并行的抓取 URL。
	// TODO: 不重复抓取页面。
	// 下面并没有实现上面两种情况：

	//先判断锁是否可用，下面的代码可以实现线程安全的map
	defer known.wg.Done()

	known.mux.Lock()
	if known.smap[url] {
		known.mux.Unlock()
		return
	} else {
		known.smap[url] = true
		known.mux.Unlock()
	}

	// 函数推出证明go程的任务完成了，需要减少计数器

	if depth <= 0 {
		return
	}
	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	known.wg.Add(len(urls))
	for _, u := range urls {
		go Crawl(u, depth-1, fetcher, known)
	}
	return
}

func main() {
	known := Safemap{smap: make(map[string]bool)}
	known.wg.Add(1)
	go Crawl("https://golang.org/", 4, fetcher, &known)
	known.wg.Wait()
}

type Safemap struct {
	smap map[string]bool
	mux  sync.Mutex
	wg   sync.WaitGroup
}

// fakeFetcher 是返回若干结果的 Fetcher。
type fakeFetcher map[string]*fakeResult

type fakeResult struct {
	body string
	urls []string
}

func (f fakeFetcher) Fetch(url string) (string, []string, error) {
	if res, ok := f[url]; ok {
		return res.body, res.urls, nil
	}
	return "", nil, fmt.Errorf("not found: %s", url)
}

